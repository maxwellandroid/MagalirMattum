public class HomePage extends AppCompatActivity
        implements NavigationView.OnNavigationItemSelectedListener {

    String url="http://www.maxwellstreaming.com/hls/hlsurl.php";
    int aspectRatio = VideoInfo.AR_ASPECT_FILL_PARENT;


    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_home_page);
        Toolbar toolbar = (Toolbar) findViewById(R.id.toolbar);
        setSupportActionBar(toolbar);


        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        ActionBarDrawerToggle toggle = new ActionBarDrawerToggle(
                this, drawer, toolbar, R.string.navigation_drawer_open, R.string.navigation_drawer_close);
        drawer.addDrawerListener(toggle);
        toggle.syncState();

        NavigationView navigationView = (NavigationView) findViewById(R.id.nav_view);
        navigationView.setNavigationItemSelectedListener(this);
        navigationView.getMenu().getItem(0).setChecked(true);

        LiveNewsFragment liveNewsFragment = new LiveNewsFragment();
        insertFragment(liveNewsFragment);
    }

    @Override
    public void onBackPressed() {
        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        if (drawer.isDrawerOpen(GravityCompat.START)) {
            drawer.closeDrawer(GravityCompat.START);
        } else {
            super.onBackPressed();
            finish();
        }
    }

    @SuppressWarnings("StatementWithEmptyBody")
    @Override
    public boolean onNavigationItemSelected(MenuItem item) {
        // Handle navigation view item clicks here.
        int id = item.getItemId();

        if (id == R.id.nav_home) {

            LiveNewsFragment liveNewsFragment = new LiveNewsFragment();
            insertFragment(liveNewsFragment);
            // Handle the camera action
        } else if (id == R.id.nav_programs) {

            ProgrammsFragment programmsFragment = new ProgrammsFragment();
            insertFragment(programmsFragment);

        } else if (id == R.id.nav_contact_us) {
            ContactUsFragment contactUsFragment = new ContactUsFragment();
            insertFragment(contactUsFragment);
        }

        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        drawer.closeDrawer(GravityCompat.START);
        return true;
    }

    private void insertFragment(Fragment fragment) {
        FragmentManager manager = getSupportFragmentManager();
        FragmentTransaction transaction = manager.beginTransaction();
        transaction.replace(R.id.frame, fragment);
        transaction.addToBackStack("mainstack");
        transaction.commit();
    }



}
